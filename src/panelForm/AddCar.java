    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here
        String merek = mrk.getText();
        String tp = tipe.getText();
        String tahun =(String) thn.getSelectedItem();
        String no = NoPol.getText();
        int hrg = parseInt(Harga.getText());
        String sts = (String) status.getSelectedItem();
        TambahMobil tbh = new TambahMobil(merek,tp,tahun,no,hrg,sts);
        ImCrudMobil save = new ImCrudMobil();
         if(mrk.getText().equals("")||tipe.getText().equals("")||NoPol.getText().equals("")||Harga.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Isi Data Dengan Benar!!!");
            mrk.requestFocus();
        }else{
            try {
              save.simpan(tbh);
              tablemobil.setModel(new ImCrudMobil().tampil());
              reset();
            } catch (Exception ex) {
              Logger.getLogger(AddCar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
          
    }//GEN-LAST:event_btnTambahActionPerformed

    private void tablemobilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablemobilMouseClicked
        // TODO add your handling code here:
         int baris = tablemobil.rowAtPoint(evt.getPoint());
         tamp = tablemobil.getValueAt(baris, 0).toString();
         id=parseInt(tamp);
         mrk.setText(tablemobil.getValueAt(baris, 1).toString());
         tipe.setText(tablemobil.getValueAt(baris, 2).toString());
         thn.setSelectedItem(tablemobil.getValueAt(baris, 3).toString());
         NoPol.setText(tablemobil.getValueAt(baris, 4).toString());
         Harga.setText(tablemobil.getValueAt(baris, 5).toString());
         status.setSelectedItem(tablemobil.getValueAt(baris, 6).toString());
         btnTambah.setEnabled(false);
    }//GEN-LAST:event_tablemobilMouseClicked

