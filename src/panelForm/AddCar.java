    private void mrkKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_mrkKeyPressed
        char karakter = evt.getKeyChar();
        String nama = mrk.getText();
        int panjangnama = nama.length();
        if (val.valNama(karakter, panjangnama)==false){
            mrk.setEditable(false);
        }else{
            if(val.valNamaBatas(karakter, panjangnama)==true){
                mrk.setEditable(true);
                if(panjangnama>=0&&panjangnama<35){
                    if(panjangnama==30){
                        mrk.setEditable(false);
                    }
                }
                mrk.setEditable(true);
            }else{
                mrk.setEditable(false);
            }
        }
    }//GEN-LAST:event_mrkKeyPressed

