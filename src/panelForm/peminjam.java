    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String ktp,nama,telp,alamat;
        ktp=noktp.getText();
        nama=txtnama.getText();
        telp=txttelp.getText();
        alamat=txtalamat.getText();
        EntitasPeminjam obj = new EntitasPeminjam(ktp,nama,telp,alamat);
        CrudPeminjam simpan = new CrudPeminjam();
        if(txtalamat.getText().equals("Alamat...")||noktp.getText().equals("")||txtnama.getText().equals("")||txttelp.getText().equals("")||txtalamat.getText().equals("")||cektlp==false||cekNik==false){
            JOptionPane.showMessageDialog(null, "Isi Data Dengan Benar!!!");
            noktp.requestFocus();
        }else{
            try {
              simpan.simpan(obj);
              reset();
              jPanel1.setVisible(false);
              jLabel10.setText(ktp);
              jLabel11.setText(nama);
              jLabel12.setText(telp);
              jLabel13.setText(alamat);
              nyalapanel();
            } catch (Exception ex) {
              Logger.getLogger(AddCar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_simpanActionPerformed

