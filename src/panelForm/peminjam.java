    private void noktpKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_noktpKeyPressed
        // TODO add your handling code here:
        char karakter = evt.getKeyChar();
        String noNik = noktp.getText();
        int panjangNik = noNik.length();
        if (val.valNik(karakter, panjangNik)==false) {
            noktp.setEditable(false);
            cekNik=false;
        }else{
            if(val.valbatas(karakter, panjangNik)==true){
                noktp.setEditable(true);
                if(panjangNik>=0&&panjangNik<=15){
                    if(panjangNik==15){
                        cekNik=true;
                    }else{
                        cekNik=false;
                    }
                }

            }else{
                noktp.setEditable(false);
            }

        }
    }//GEN-LAST:event_noktpKeyPressed

