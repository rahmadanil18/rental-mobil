    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here
        String Name =jLabel11.getText();
        String number = (String) NO_MOBIL.getSelectedItem();
        SimpleDateFormat waktuformat = new SimpleDateFormat("dd/MM/yyyy");
        String Pinjam = waktuformat.format(rental.getDate());
        String Kembali = waktuformat.format(kembali.getDate());;
        int harga = (int) tarif;
        int waktu = (int) lama;
        int tarif = (int) total;
        EnTransaksi transaksi = new EnTransaksi(Name,number,Pinjam,Kembali,harga,waktu,tarif);
        CrudTransaksi save = new CrudTransaksi();
        if(rental.getDate()==kembali.getDate()){
            JOptionPane.showMessageDialog(null, "Minimal Peminjaman 1 !!!");
        }else{
            try {
                if (JOptionPane.showConfirmDialog(null, "Are you sure save data?","Confirmation",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE)== JOptionPane.YES_OPTION){
                    mati();
                    save.simpan(transaksi);
                    mobil.editStatus(number);
                    mobil.tampil();
                    jPanel1.setVisible(false);
                    NO_MOBIL.setEditable(false);
                    notah(Name);
                    bersih();
                    this.setVisible(false);
                    start();
                }else{
                    start();
                }
               
            } catch (Exception ex) {
                Logger.getLogger(peminjam.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tambahActionPerformed

